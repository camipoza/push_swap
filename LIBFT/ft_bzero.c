#include "libft.h"
#include <stdio.h>

void	ft_bzero(void *s, size_t n) //erases data, sets byte to 0 in a string
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
